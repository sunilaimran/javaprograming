package day09_IfStatments;

public class posNegZero {
    public static void main(String[] args) {

        int n = 100;
        if(n > 0) {
            System.out.println("positive");
        }

        if(n < 0 ) {
            System.out.println("negative");
        }


        if(n == 0) {
            System.out.println("zero");
        }
        System.out.println("---------------------------------");



        int n1 = 100;

        if(n1 > 0){
            System.out.println("positive");
        }else if(n1 < 0){
            System.out.println("negative");
        }else{
            System.out.println("zero");
        }







    }










}
